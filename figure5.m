function [beta1, beta2, beta3, beta4] = compute_beta(E, t1L, t2L, dLD, dLU, epsU, epsD, alphaL, d1D, d1U, t1R, t2R, alphaR, dRD, dRU)
     den = t1L * t2L - dLD * dLU;            
    a3 = (-t2L * (E - epsU) * exp(-1i*alphaL) - t1L * (E - epsD) * exp(1i*alphaL) - d1U*dLD - d1D*dLU) / den;
    a2 = (exp(-1i*(alphaL-alphaR)) * t1R * t2L + exp(1i*(alphaL-alphaR)) * t1L * t2R ...
          + E^2 + (-epsD-epsU)*E - d1D*d1U - dLD*dRU - dLU*dRD + epsU*epsD) / den;
    a1 = (-t2R * (E - epsU) * exp(-1i*alphaR) - t1R * (E - epsD) * exp(1i*alphaR) - d1U*dRD - d1D*dRU) / den;
    Y  = (exp(-1i*(alphaL-alphaR)) * t1R * t2L + exp(1i*(alphaL-alphaR)) * t1L * t2R - dLD*dRU - dLU*dRD) / den;
    
    K = sqrt(a3^2/4 - a2 + Y);
    alpha0 = a3/2;
    A1 = sqrt(3*a3^2/4 - K^2 - 2*a2 + (4*a3*a2 - 8*a1 - a3^3) / (4*K));
    A2 = sqrt(3*a3^2/4 - K^2 - 2*a2 - (4*a3*a2 - 8*a1 - a3^3) / (4*K)); 

    beta1 = -(alpha0 - K - A1)/2;
    beta2 = -(alpha0 - K + A1)/2;
    beta3 = -(alpha0 + K - A2)/2;
    beta4 = -(alpha0 + K + A2)/2;
end

function z_out = break_large_jumps(z_in, fac)
    
    z_out = z_in;
    dz = abs(diff(z_in));
    dz_valid = dz(isfinite(dz) & dz > 0);

    if isempty(dz_valid)
        return;
    end

    thr = fac * median(dz_valid);
    bad = find(dz > thr);

    for ii = 1:numel(bad)
        z_out(bad(ii)+1) = NaN;
    end
end

function draw_patch_open_curve(z, lw)
    z = z(:);
    M1 = 6000; 

    c_all = linspace(0,1,M1);
    c_all = [c_all, c_all(2:end)]; 
    c_all = c_all(1:numel(z)).';

    isBreak = isnan(z);
    start_idx = [1; find(isBreak)+1];
    end_idx   = [find(isBreak)-1; numel(z)];

    valid_block = start_idx <= end_idx;
    start_idx = start_idx(valid_block);
    end_idx   = end_idx(valid_block);

    for b = 1:numel(start_idx)
        ids = start_idx(b):end_idx(b);
        if numel(ids) < 2, continue; end
        zb = z(ids);
        xb = real(zb); yb = imag(zb); cb = c_all(ids);
        vertices = [xb, yb, zeros(numel(zb),1)];
        faces = [(1:numel(zb)-1)', (2:numel(zb))'];
        patch('Vertices', vertices, 'Faces', faces, ...
              'FaceVertexCData', cb, 'FaceColor', 'none', ...
              'EdgeColor', 'interp', 'LineWidth', lw);
    end
end
