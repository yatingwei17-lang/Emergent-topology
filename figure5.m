             exp(1i*alphaL) * beta_curr^3 * t1L * epsD + ...
             exp(1i*alphaR) * beta_curr * t1R * epsD + ...
             (t1L*t2L - dLD*dLU) * beta_curr^4 + ...
             (-d1U*dLD - d1D*dLU) * beta_curr^3 + ...
             (-d1U*d1D - dLD*dRU - dLU*dRD + epsD*epsU) * beta_curr^2 + ...
             (-d1U*dRD - d1D*dRU) * beta_curr - ...
             dRD*dRU + t1R*t2R ...
             ) / beta_curr^2;
        Ej_candidates = roots([c2, c1, c0]); 
        for m = 1:numel(Ej_candidates)
