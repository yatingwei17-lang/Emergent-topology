                (-t2R*(E_test-epsU)*exp(-1i*alphaR) - t1R*(E_test-epsD)*exp(1i*alphaR) - d1U*dRD - d1D*dRU), ...
                (t1R*t2R - dRD*dRU)];
            all_betas = roots(p_verify);
            all_mags = abs(all_betas);
            mags_sorted = sort(all_mags);
            condition_GBZ = abs(mags_sorted(2) - mags_sorted(3)) < 1e-6; 
            curr_mag = abs(beta_curr);
            condition_is_on_GBZ = abs(curr_mag - mags_sorted(2)) < 1e-6 || ...
                                  abs(curr_mag - mags_sorted(3)) < 1e-6;
            if condition_GBZ && condition_is_on_GBZ
